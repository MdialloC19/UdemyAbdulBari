public class ChalengePatterns1 {
    public static void main(String [] args){
        System.out.println("Chalenge Pattern1");

        for( int i=1;i<=5;i++){
            for(int j=1; j<=5; j++){
                System.out.print(j+ " "); 
            }
            System.out.println("");
        }

        System.out.println("Chalenge Pattern2");

        for( int i=1;i<=5;i++){
            for(int j=1; j<=5; j++){
                System.out.print(i+ " "); 
            }
            System.out.println("");
        }

        System.out.println("Chalenge Pattern 3");

        for( int i=1;i<=5;i++){
            for(int j=1; j<=5; j++){
                System.out.print(i+j+ " "); 
            }
            System.out.println("");
        }
    }
    
}
